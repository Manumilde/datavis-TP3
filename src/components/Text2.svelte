<h2 style="text-align:center">El versus</h2>

<p>Marvel y DC han sido a lo largo de los años los mayores exponentes cuando hablamos de los comics. 
    Ambos incursionaron en el mundo del cine desde el siglo pasado, DC cosechaba exitos ocasionales pero Marvel iba de fracaso en fracaso. </p>

<p>Ya con los derechos vendidos de varios de sus iconicos personajes (como X-men a Fox y Spiderman a Sony), 
    Marvel decide reactivar su propio estudio y producir “con lo que le quedaba”. </p>


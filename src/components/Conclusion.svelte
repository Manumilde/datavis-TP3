
<h2 style="text-align:center">
    Atando Cabos
</h2>
<p>
    Las peliculas de superheroes, si tomamos como base los datos estadísticos, no están encaminadas a desaparecer.
    Pero debido a una combinacion de factores como la Pandemia del 2020 y el aumento en la oferta de los servicios de streaming,
    el rendimiento de estas películas volvió a ser el que tenían previo a esta "Época Dorada" (2012-2019).
</p>
<p>Tampoco notamos complicaciones en su rendimiento en la crítica. El problema ya puede relacionarse más con una crisis del cine en sí (por ejemplo, menor asistencia a las salas),
     sobre cómo el streaming se elige como primera opción por los consumidores para ver este tipo de contenidos y por los ejecutivos para lanzar sus producciones.</p>


<p>En 2008 Marvel Studios lanza Iron Man, 
    una pelicula de la que no se esperaba mucho por su personaje poco popular en la pantalla, pero dejo una influencia notable. 
    Lo mas importante fue que introdujeron un sistema de conexion entre largometrajes que no habia sido utilizado en “Blockbusters”, 
    comenzando así su “Universo Cinematografico” (MCU).</p>